    private void innitComboBox() {       
        try {
            conn = connectDB.getDBConnect();
            stm = conn.prepareStatement(sql);
            rs = stm.executeQuery();
            cbx_maLopMh1.removeAllItems();
            while (rs.next()) {
                cbx_maLopMh1.addItem(rs.getString("MaLopMH"));
            }
            rs.close();
            stm.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

